package common;

/**
 * @author Anthony
 * This enum stores which Simulator types are available in the package.
 */
public enum Simulator_Types {
	/**
	 * The primitive double array simulator.
	 */
	Tpdahp,
	
	/**
	 * The wrapped Double array simulator.
	 */
	Twdahp,
	
	/**
	 * The wrapped Fouble array simulator.
	 */
	Twfahp,
	
	/**
	 * The primitive float array simulator.
	 */
	Tpfahp,
	
	/**
	 * The primitive double object web simulator.
	 */
	Tpdohp
}
